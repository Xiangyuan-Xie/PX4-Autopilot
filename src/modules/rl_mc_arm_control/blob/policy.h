template <std::size_t InDim, std::size_t OutDim>
inline void linear(const std::array<float, InDim>& input,
	const std::array<float, OutDim * InDim>& weights,
	const std::array<float, OutDim>& bias,
	std::array<float, OutDim>& output)
{
	for (std::size_t row = 0; row < OutDim; ++row) {
		float acc = bias[row];
		for (std::size_t col = 0; col < InDim; ++col) {
			acc += weights[row * InDim + col] * input[col];
		}
		output[row] = acc;
	}
}

template <std::size_t Dim>
inline void apply_elu(std::array<float, Dim>& values)
{
	for (float& value : values) {
		if (value < 0.0f) {
			value = std::expm1(value);
		}
	}
}

inline void normalize(std::array<float, kObservationDim>& values)
{
	if (!kUseObservationNormalization) {
		return;
	}
	for (std::size_t i = 0; i < kObservationDim; ++i) {
		values[i] = (values[i] - kObservationMean[i]) * kObservationInvStd[i];
	}
}

inline void gru_step(const std::array<float, kGruInputDim>& input, std::array<float, kGruHiddenDim>& hidden)
{
	std::array<float, kGruHiddenDim> reset_gate{};
	std::array<float, kGruHiddenDim> update_gate{};
	std::array<float, kGruHiddenDim> new_gate{};
	for (std::size_t i = 0; i < kGruHiddenDim; ++i) {
		float ir = kGruBiasIH[i];
		float iz = kGruBiasIH[kGruHiddenDim + i];
		float in = kGruBiasIH[2 * kGruHiddenDim + i];
		float hr = kGruBiasHH[i];
		float hz = kGruBiasHH[kGruHiddenDim + i];
		float hn = kGruBiasHH[2 * kGruHiddenDim + i];
		for (std::size_t j = 0; j < kGruInputDim; ++j) {
			ir += kGruWeightIH[i * kGruInputDim + j] * input[j];
			iz += kGruWeightIH[(kGruHiddenDim + i) * kGruInputDim + j] * input[j];
			in += kGruWeightIH[(2 * kGruHiddenDim + i) * kGruInputDim + j] * input[j];
		}
		for (std::size_t j = 0; j < kGruHiddenDim; ++j) {
			hr += kGruWeightHH[i * kGruHiddenDim + j] * hidden[j];
			hz += kGruWeightHH[(kGruHiddenDim + i) * kGruHiddenDim + j] * hidden[j];
			hn += kGruWeightHH[(2 * kGruHiddenDim + i) * kGruHiddenDim + j] * hidden[j];
		}
		reset_gate[i] = 1.0f / (1.0f + std::exp(-(ir + hr)));
		update_gate[i] = 1.0f / (1.0f + std::exp(-(iz + hz)));
		new_gate[i] = std::tanh(in + reset_gate[i] * hn);
	}
	for (std::size_t i = 0; i < kGruHiddenDim; ++i) {
		hidden[i] = (1.0f - update_gate[i]) * new_gate[i] + update_gate[i] * hidden[i];
	}
}

inline void reset(std::array<float, kGruHiddenDim>& hidden)
{
	hidden.fill(0.0f);
}

inline void evaluate(
	const std::array<float, kObservationDim>& raw_observation,
	std::array<float, kGruHiddenDim>& hidden,
	std::array<float, kActionDim>& action)
{
	std::array<float, kObservationDim> normalized = raw_observation;
	normalize(normalized);

	{
	std::array<float, 16> actor_encoder_layer_0{};
	linear<35, 16>(normalized, actor_encoder_weight_0, actor_encoder_bias_0, actor_encoder_layer_0);
	apply_elu(actor_encoder_layer_0);
	std::array<float, 16> actor_encoder_layer_1{};
	linear<16, 16>(actor_encoder_layer_0, actor_encoder_weight_1, actor_encoder_bias_1, actor_encoder_layer_1);
	apply_elu(actor_encoder_layer_1);
	std::array<float, 16> output{};
	linear<16, 16>(actor_encoder_layer_1, actor_encoder_weight_2, actor_encoder_bias_2, output);
		gru_step(output, hidden);
	}
	{
	std::array<float, 4> output{};
	linear<16, 4>(hidden, actor_head_weight_0, actor_head_bias_0, output);
		action = output;
	}
}
